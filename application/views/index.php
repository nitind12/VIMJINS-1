      <h2>Submission Guidelines</h2>
      <p>
         <ol>
          <li>Manuscripts have to be mailed to <a href="mailto:vimjins@gmail.com">vimjins@gmail.com</a>. Please use the manuscript-title in the Subject Line. The length of the manuscript must not exceed 7000 words.</li>
          <li>All author-identifying information should be removed from the manuscript.</li>
          <li>The Title Page should be sent as a separate file named ‘Title Page’ and include the following information-
            <ul>
              <li>Title of the Article</li>
              <li>Name of the Author(s)</li>
              <li>Corresponding Author</li>
              <li>Affiliation (department, institution, city, state, country) & Contact Information (Phone Number & E-mail IDs) of the Author(s)</li>
              <li>Language of the Article (English/ Hindi)</li>
            </ul>
          </li>
          <li>The Manuscript should be sent as a separate file. The title should be mentioned at the top followed by an abstract of around 200 words. Abstract should be followed by 3-5 keywords in alphabetical order following which the main text can be started.</li>
          <li>Formatting: Manuscript formatting should be done as per the following-
            <ul>
              <li>A4 Size, Single column, Margin of 1-inch (2.54 cm) on all sides </li>
              <li>Font: Kruti Dev, Font Size: 14 (Hindi) or Times New Roman (12)</li>
              <li>Justified Alignment, 1.5 Line Spacing, Space between paragraphs</li>
              <li>Title, Sub-titles, sub-sub-titles etc. in Bold</li>
              <li>with first letter of each word Capitalized.</li>
            </ul>
          </li>
          <li>Figures, tables, diagrams, photographs etc. should be placed in the main text close to where they have been referred to first. They should be numbered in the order of appearance as Illustration 1, Illustration 2 and so on. The same nomenclature should be used to refer to them in the text.
          <li>Referencing: APA style
          <li>No fee is to be paid for publication
         <li>Editorial Board’s decision shall be communicated within 5 weeks from the article submission deadline.
          <li>Submission Deadline: 30th June, 2021
          <li>Authors may address their queries to vimjins@gmail.com
        </ol>
      </p>
